//=============================================================================
// [~CCursor] デストラクタ
//=============================================================================
CCursor * CCursor::Create(D3DXVECTOR3 pos)
{
    //CGridのポインタ作成
    CCursor *Cursor;
    //インスタンスを生成
    Cursor = new CCursor;
    //NULLチェック
    if (Cursor != NULL)
    {
        //それぞれの初期化処理
        Cursor->Init();
        Cursor->SetPos(pos);
        Cursor->SetSize({ GRID_SIZE,0.0f,GRID_SIZE });// カーソルのサイズはグリッドのサイズに合わせる
    }
    //ポインタを返す
    return Cursor;
}

//=============================================================================
// [Init] 入力処理
//=============================================================================
HRESULT CCursor::Init(void)
{
    CScene3d::Init();
    SetColor({ 1.0f,0.3f,0.3f,1.0f });
    return S_OK;
}

