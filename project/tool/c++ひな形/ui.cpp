//=============================================================================
// [Create] オブジェクトの生成
//=============================================================================
CUI* CUI::Create(HWND hWnd)
{
    CUI *pUi = NULL;
    if (!pUi)
    {
        pUi = new CUI;
        pUi->Init(hWnd);
    }
    return pUi;
}

//=============================================================================
// [Init] 初期化処理
//=============================================================================
void CUI::Init(HWND hWnd)
{


    // IMGUIの設定
#ifdef IMGUI_ON
    IMGUI_CHECKVERSION();
    ImGui::CreateContext();
    ImGuiIO& io = ImGui::GetIO(); (void)io;

    ImGui::StyleColorsDark();

    ImGui_ImplWin32_Init(hWnd);
    ImGui_ImplDX9_Init(m_pRenderer->GetDevice());
#endif // IMGUI
}

